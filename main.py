# ===================== 2. Core Game Environment Class (Fixed Rules, Assignment Compliant) =====================
class MazeGame:
    def __init__(self):
        # Map configuration: 5x5 grid, discrete state space for basic Q-Learning
        self.grid_size = 5
        self.start_pos = (0, 0)  # Start point: top-left corner
        self.goal_pos = (4, 4)  # Goal (treasure): bottom-right corner, core objective
        self.traps = [(1, 1), (2, 3), (3, 0)]  # Fixed obstacle traps
        self.current_pos = self.start_pos  # Agent's current position
        self.max_steps = 30  # Max steps per episode, prevent infinite loop
        self.current_step = 0  # Steps taken in current episode

    # Reset game: call before each new episode, return to initial state
    def reset(self):
        self.current_pos = self.start_pos
        self.current_step = 0
        return self._pos_to_state(self.current_pos)

    # Convert (x,y) coordinates to discrete state ID (0-24, total 25 states)
    def _pos_to_state(self, pos):
        x, y = pos
        return x * self.grid_size + y

    # Core step function: input action, return [next_state, immediate_reward, episode_done_flag]
    def step(self, action):
        x, y = self.current_pos
        new_x, new_y = x, y

        # Action space: 4 discrete actions with fixed encoding
        if action == 0:  # Up: x decreases by 1, no out-of-bounds
            new_x = max(x - 1, 0)
        elif action == 1:  # Down: x increases by 1
            new_x = min(x + 1, self.grid_size - 1)
        elif action == 2:  # Left: y decreases by 1
            new_y = max(y - 1, 0)
        elif action == 3:  # Right: y increases by 1
            new_y = min(y + 1, self.grid_size - 1)

        # Increment step count
        self.current_step += 1
        new_pos = (new_x, new_y)
        # Reward function: hierarchical positive/negative rewards to guide optimal behavior
        reward = -1  # Default -1 per step, encourage shortest path
        done = False  # Episode completion flag

        # 1. Out-of-bounds penalty: negative reward for hitting grid boundary
        if new_pos == self.current_pos:
            reward = -10
        # 2. Trap penalty: high negative reward, end episode immediately
        elif new_pos in self.traps:
            reward = -50
            done = True
        # 3. Goal reward: high positive reward for reaching treasure, end episode
        elif new_pos == self.goal_pos:
            reward = 100
            done = True
        # 4. Max steps exceeded: end episode to prevent loop
        elif self.current_step >= self.max_steps:
            done = True

        # Update current position
        self.current_pos = new_pos
        # Return core data: next state, reward, episode done flag
        return self._pos_to_state(new_pos), reward, done


# ===================== 3. Q-Learning Agent Class (With Performance Tracking) =====================
class QLearningAgent:
    def __init__(self, state_size, action_size, alpha=0.1, gamma=0.95, epsilon=1.0):
        # Basic configuration
        self.state_size = state_size  # Total states: 5x5=25
        self.action_size = action_size  # Total actions: 4
        # Initialize Q-Table: [states x actions] array, all zeros initially
        # Stores the value of each state-action pair
        self.q_table = np.zeros((state_size, action_size))

        # Hyperparameter configuration (tunable for optimization experiments)
        self.alpha = alpha  # Learning rate: controls the magnitude of each Q-value update (0-1)
        self.gamma = gamma  # Discount factor: weights future rewards, closer to 1 = focus on long-term gain
        self.epsilon = epsilon  # Initial exploration rate: 1.0 = full random exploration at start
        self.epsilon_min = 0.01  # Minimum exploration rate: prevent zero exploration in late training
        self.epsilon_decay = 0.995  # Exploration decay rate: balance exploration & exploitation per episode

        # Performance tracking data (core data source for assignment report)
        self.reward_history = []  # Total reward per episode
        self.step_history = []  # Total steps per episode
        self.epsilon_history = []  # Exploration rate per episode

    # Action selection: epsilon-greedy strategy (core assignment requirement)
    def choose_action(self, state, is_test=False):
        # Test mode: full exploitation, select action with max Q-value (no random exploration)
        if is_test:
            return np.argmax(self.q_table[state, :])
        # Training mode: explore with epsilon probability, exploit with 1-epsilon probability
        if np.random.uniform(0, 1) < self.epsilon:
            return np.random.choice(self.action_size)  # Exploration: random action
        else:
            return np.argmax(self.q_table[state, :])  # Exploitation: action with max Q-value

    # Q-Table update: core Bellman Equation (algorithm core)
    def update_q_table(self, state, action, reward, next_state, done):
        # Current Q-value of the state-action pair
        q_current = self.q_table[state, action]
        # Target Q-value: only immediate reward if episode ends, else add discounted future max Q-value
        if done:
            q_target = reward
        else:
            q_target = reward + self.gamma * np.max(self.q_table[next_state, :])
        # Incremental Q-value update
        self.q_table[state, action] = q_current + self.alpha * (q_target - q_current)

    # Decay exploration rate: call after each episode, gradually reduce exploration
    def decay_epsilon(self):
        if self.epsilon > self.epsilon_min:
            self.epsilon *= self.epsilon_decay
        # Record current epsilon for performance evaluation
        self.epsilon_history.append(self.epsilon)

    # Record episode data: call after each episode for performance tracking
    def record_episode_data(self, total_reward, total_step):
        self.reward_history.append(total_reward)
        self.step_history.append(total_step)


