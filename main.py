# 4.1 Training Convergence Curve Plot: Reward, Steps, Epsilon (core report charts)
def plot_training_curve(agent, save_fig=False):
    """
    Plot training performance curves for assignment report
    :param agent: Trained QLearningAgent instance
    :param save_fig: Whether to save figure to local file
    """
    # Calculate moving average reward for smoother convergence trend
    window_size = 50
    smoothed_rewards = np.convolve(agent.reward_history, np.ones(window_size) / window_size, mode='valid')

    # Create 3 subplots
    fig, axes = plt.subplots(3, 1, figsize=(12, 15))

    # Subplot 1: Total Reward Curve
    axes[0].plot(agent.reward_history, label='Episode Total Reward', alpha=0.6, color='lightblue')
    axes[0].plot(range(window_size - 1, len(agent.reward_history)), smoothed_rewards,
                 label=f'{window_size}-Episode Moving Avg Reward', color='darkblue', linewidth=2)
    axes[0].set_title('1. Training Total Reward Trend', fontsize=14, fontweight='bold')
    axes[0].set_xlabel('Training Episodes', fontsize=12)
    axes[0].set_ylabel('Total Reward', fontsize=12)
    axes[0].legend()
    axes[0].grid(True, alpha=0.3)

    # Subplot 2: Steps per Episode Curve
    axes[1].plot(agent.step_history, color='orange', alpha=0.7)
    axes[1].set_title('2. Training Steps per Episode Trend', fontsize=14, fontweight='bold')
    axes[1].set_xlabel('Training Episodes', fontsize=12)
    axes[1].set_ylabel('Total Steps per Episode', fontsize=12)
    axes[1].grid(True, alpha=0.3)

    # Subplot 3: Epsilon Decay Curve
    axes[2].plot(agent.epsilon_history, color='green', linewidth=2)
    axes[2].set_title('3. Training Exploration Rate (Epsilon) Decay', fontsize=14, fontweight='bold')
    axes[2].set_xlabel('Training Episodes', fontsize=12)
    axes[2].set_ylabel('Exploration Rate Epsilon', fontsize=12)
    axes[2].grid(True, alpha=0.3)

    plt.tight_layout()
    if save_fig:
        plt.savefig('model_performance_convergence_curve.png', dpi=300, bbox_inches='tight')
    plt.show()

    # Print quantitative evaluation metrics (direct use for report)
    print("=" * 50)
    print("Model Performance Quantitative Metrics")
    print("=" * 50)
    print(f"Total Training Episodes: {len(agent.reward_history)}")
    print(f"Avg Reward (First 100 Episodes): {np.mean(agent.reward_history[:100]):.2f}")
    print(f"Avg Reward (Last 100 Episodes): {np.mean(agent.reward_history[-100:]):.2f}")
    print(f"Avg Steps (First 100 Episodes): {np.mean(agent.step_history[:100]):.2f}")
    print(f"Avg Steps (Last 100 Episodes): {np.mean(agent.step_history[-100:]):.2f}")
    print(f"Minimum Steps (Optimal Path): {np.min(agent.step_history)}")
    print(f"Final Exploration Rate: {agent.epsilon:.4f}")
    print("=" * 50)


# 4.2 Hyperparameter Tuning Experiment (for assignment report)
def hyperparameter_tuning_experiment(train_episodes=1000):
    """
    Hyperparameter tuning comparison experiment
    Compare training performance of different learning rate and discount factor
    :param train_episodes: Number of training episodes
    """
    # Hyperparameter combinations to test
    alpha_list = [0.05, 0.1, 0.2]
    gamma_list = [0.9, 0.95, 0.99]
    game = MazeGame()
    state_size = game.grid_size * game.grid_size
    action_size = 4

    # Store reward history for different hyperparameters
    reward_results = {}

    # Iterate all hyperparameter combinations
    for alpha in alpha_list:
        for gamma in gamma_list:
            print(f"Training: Learning Rate alpha={alpha}, Discount Factor gamma={gamma}")
            agent = QLearningAgent(state_size, action_size, alpha=alpha, gamma=gamma)
            # Training loop
            for episode in range(train_episodes):
                state = game.reset()
                total_reward = 0
                done = False
                while not done:
                    action = agent.choose_action(state)
                    next_state, reward, done = game.step(action)
                    agent.update_q_table(state, action, reward, next_state, done)
                    state = next_state
                    total_reward += reward
                agent.record_episode_data(total_reward, game.current_step)
                agent.decay_epsilon()
            # Save results
            key = f"alpha={alpha}, gamma={gamma}"
            reward_results[key] = agent.reward_history

    # Plot comparison curve
    plt.figure(figsize=(14, 8))
    window_size = 50
    for key, reward_history in reward_results.items():
        smoothed_rewards = np.convolve(reward_history, np.ones(window_size) / window_size, mode='valid')
        plt.plot(range(window_size - 1, len(reward_history)), smoothed_rewards, label=key, linewidth=2)

    plt.title('Convergence Comparison of Different Hyperparameter Combinations', fontsize=16, fontweight='bold')
    plt.xlabel('Training Episodes', fontsize=14)
    plt.ylabel(f'{window_size}-Episode Moving Avg Reward', fontsize=14)
    plt.legend(fontsize=12)
    plt.grid(True, alpha=0.3)
    plt.tight_layout()
    plt.savefig('hyperparameter_tuning_comparison.png', dpi=300, bbox_inches='tight')
    plt.show()


# ===================== 5. Local GUI Interface (PyCharm Run, Single-Step Test Bug Fixed) =====================
class GameUI:
    def __init__(self, root, game, agent):
        self.root = root
        self.root.title("Q-Learning Maze Treasure Hunt Game")
        self.root.geometry("800x600")
        self.root.resizable(False, False)

        # Game and agent instances
        self.game = game
        self.agent = agent
        self.is_training = False
        self.is_trained = False  # Training status flag (core fix for single-step test bug)
        self.train_episodes = 1000
        self.current_episode = 0

        # UI component configuration
        self.cell_size = 80
        self.canvas = tk.Canvas(root, width=400, height=400, bg="white", bd=2, relief="solid")
        self.canvas.place(x=50, y=50)

        # Info panel
        self.info_frame = ttk.LabelFrame(root, text="Runtime Info")
        self.info_frame.place(x=500, y=50, width=250, height=400)

        # Info labels
        self.episode_label = ttk.Label(self.info_frame, text="Episodes: 0")
        self.episode_label.pack(pady=10, anchor="w")
        self.step_label = ttk.Label(self.info_frame, text="Current Steps: 0")
        self.step_label.pack(pady=10, anchor="w")
        self.reward_label = ttk.Label(self.info_frame, text="Total Reward: 0")
        self.reward_label.pack(pady=10, anchor="w")
        self.action_label = ttk.Label(self.info_frame, text="Current Action: -")
        self.action_label.pack(pady=10, anchor="w")
        self.epsilon_label = ttk.Label(self.info_frame, text="Exploration Rate: 1.00")
        self.epsilon_label.pack(pady=10, anchor="w")

        # Button panel
        self.btn_frame = ttk.Frame(root)
        self.btn_frame.place(x=50, y=480, width=700, height=80)

        self.train_btn = ttk.Button(self.btn_frame, text="Start Training", command=self.start_train)
        self.train_btn.grid(row=0, column=0, padx=20, pady=10)

        self.test_btn = ttk.Button(self.btn_frame, text="Single-Step Test", command=self.single_step_test)
        self.test_btn.grid(row=0, column=1, padx=20, pady=10)

        self.reset_btn = ttk.Button(self.btn_frame, text="Reset Game", command=self.reset_game)
        self.reset_btn.grid(row=0, column=2, padx=20, pady=10)

        self.plot_btn = ttk.Button(self.btn_frame, text="Plot Performance", command=self.plot_performance)
        self.plot_btn.grid(row=0, column=3, padx=20, pady=10)

        # Action name mapping
        self.action_names = ["Up", "Down", "Left", "Right"]
        # Episode total reward
        self.episode_reward = 0
        # Initial map render
        self.draw_map()

    # Draw grid map
    def draw_map(self):
        self.canvas.delete("all")
        # Draw grid cells
        for i in range(self.game.grid_size):
            for j in range(self.game.grid_size):
                x1 = j * self.cell_size
                y1 = i * self.cell_size
                x2 = x1 + self.cell_size
                y2 = y1 + self.cell_size
                # Fill color
                fill_color = "white"
                if (i, j) == self.game.start_pos:
                    fill_color = "#87CEEB"  # Start point: blue
                elif (i, j) == self.game.goal_pos:
                    fill_color = "#90EE90"  # Goal point: green
                elif (i, j) in self.game.traps:
                    fill_color = "#FF6347"  # Trap: red
                # Draw cell
                self.canvas.create_rectangle(x1, y1, x2, y2, fill=fill_color, outline="black")
        # Draw agent
        agent_x, agent_y = self.game.current_pos
        agent_x1 = agent_y * self.cell_size + 10
        agent_y1 = agent_x * self.cell_size + 10
        agent_x2 = agent_y * self.cell_size + self.cell_size - 10
        agent_y2 = agent_x * self.cell_size + self.cell_size - 10
        self.canvas.create_oval(agent_x1, agent_y1, agent_x2, agent_y2, fill="purple", tags="agent")

    # Update info panel
    def update_info(self, action=-1):
        self.episode_label.config(text=f"Episodes: {self.current_episode}")
        self.step_label.config(text=f"Current Steps: {self.game.current_step}")
        self.reward_label.config(text=f"Total Reward: {self.episode_reward:.2f}")
        self.action_label.config(text=f"Current Action: {self.action_names[action] if action != -1 else '-'}")
        self.epsilon_label.config(text=f"Exploration Rate: {self.agent.epsilon:.2f}")
        self.draw_map()
        self.root.update()

    # Reset game
    def reset_game(self):
        self.is_training = False
        self.current_episode = 0
        self.episode_reward = 0
        self.game.reset()
        # Reset Q-Table and training status
        self.agent.q_table = np.zeros((self.agent.state_size, self.agent.action_size))
        self.agent.epsilon = 1.0
        self.agent.reward_history = []
        self.agent.step_history = []
        self.agent.epsilon_history = []
        self.is_trained = False
        self.update_info()
        messagebox.showinfo("Reset", "Game and model have been reset successfully!")

    # Single-Step Test (core bug fix: training status validation)
    def single_step_test(self):
        # Core fix: block test if model is not trained
        if not self.is_trained:
            messagebox.showerror("Operation Error", "Please click 'Start Training' to complete model training first!")
            return
        # Core fix: test mode uses pure greedy strategy (no random exploration)
        state = self.game._pos_to_state(self.game.current_pos)
        action = self.agent.choose_action(state, is_test=True)
        # Execute action
        next_state, reward, done = self.game.step(action)
        self.episode_reward += reward
        # Update UI
        self.update_info(action)
        # Episode completion check
        if done:
            if self.game.current_pos == self.game.goal_pos:
                messagebox.showinfo("Test Complete", "✅ Agent successfully reached the treasure goal!")
            elif self.game.current_pos in self.game.traps:
                messagebox.showinfo("Test Complete", "❌ Agent hit a trap, episode ended!")
            else:
                messagebox.showinfo("Test Complete", "⏹️ Max steps exceeded, episode ended!")
            # Reset test environment
            self.game.reset()
            self.episode_reward = 0
            self.update_info()

    # Plot performance curve
    def plot_performance(self):
        if not self.is_trained:
            messagebox.showerror("Operation Error", "Please complete model training first to plot performance curves!")
            return
        plot_training_curve(self.agent, save_fig=True)

    # Start training
    def start_train(self):
        if self.is_training:
            return
        self.is_training = True
        self.train_btn.config(state="disabled")
        self.reset_game()
        self.current_episode = 0

        # Training loop
        for episode in range(self.train_episodes):
            self.current_episode = episode + 1
            state = self.game.reset()
            self.episode_reward = 0
            done = False

            while not done:
                # Select action
                action = self.agent.choose_action(state)
                # Execute action
                next_state, reward, done = self.game.step(action)
                # Accumulate reward
                self.episode_reward += reward
                # Update Q-Table
                self.agent.update_q_table(state, action, reward, next_state, done)
                # Switch state
                state = next_state
                # Visualize every 100 episodes to avoid UI lag
                if episode % 100 == 0:
                    self.update_info(action)
                    self.root.after(10)

            # Record episode data for performance evaluation
            self.agent.record_episode_data(self.episode_reward, self.game.current_step)
            # Decay exploration rate
            self.agent.decay_epsilon()
            # Print log every 100 episodes
            if episode % 100 == 0:
                print(
                    f"Episode:{episode + 1}, Total Reward:{self.episode_reward:.2f}, Exploration Rate:{self.agent.epsilon:.3f}")

        # Training complete
        self.is_training = False
        self.is_trained = True  # Mark training complete, unlock single-step test
        self.train_btn.config(state="normal")
        self.update_info()
        messagebox.showinfo("Training Complete",
                            f"✅ Q-Learning model training completed for {self.train_episodes} episodes!\nClick 'Single-Step Test' to test the agent, or 'Plot Performance' to view evaluation results.")


